  INTEGER     :: MAXNUM_PROFLRS	! MAX NUM PROFILERS
  INTEGER     :: MAXLVL_PROFLRS	! MAX LEVEL OF PROF

  INTEGER     :: MAXNUM_SONDES	! MAX NUM SONDES
