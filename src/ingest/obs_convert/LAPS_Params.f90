  ! LOGICAL     :: USEOBS_CLDRFWD	! USE CLOUD DRIFT WIND
  ! LOGICAL     :: USEOBS_RADARWD	! USE RADIAL WIND
  ! INTEGER     :: THRESH_RADAROB(3)	! THRESHOLD NUMBERS DOPPLER OBS/LEVEL
